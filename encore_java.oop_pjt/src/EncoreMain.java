
public class EncoreMain {

	public static void main(String[] args) {
		System.out.println("섭섭님과 함께하는 즐거운 Java");
		//여기에 변수를 선언하면 지역변수가됨 
		
		
		int    intValue			=   10;
		long	longValue 		=	intValue;
		char   	charValue 		=	'M';
		
		float 	doubleValue 	=	3.1415f;
		
		String 	StringValue 	= 	"섭섭님";
		boolean booleanValue	=	false ; 
		
		
		System.out.println("정수값 : " + intValue);
		System.out.println("문자값 : " + charValue);
		System.out.println("실수 	 : " + doubleValue);
		System.out.println("문자열 : " + StringValue);
		System.out.println("논리 	 : " + booleanValue);
		
		
		
	}

}
